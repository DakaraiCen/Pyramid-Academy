public class Passenger {
    private String name;
    private String email;
    private String phone;
    private String gender;
    private int age;

    public Passenger(String name, String email, String phone, String gender, int age){
        this.name = name;
        this.email = email;
        this.phone = phone;
        this.gender = gender;
        this.age = age;
    }
       
}
